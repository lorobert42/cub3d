#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	a;

	a = 0;
	while (a < n)
	{
		((unsigned char *)str)[a] = (unsigned char)c;
		a++;
	}
	return (str);
}
