#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	a;

	a = 0;
	while (a + 1 < size && src[a])
	{
		dst[a] = src[a];
		a++;
	}
	if (a < size)
		dst[a] = 0;
	a = 0;
	while (src[a])
		a++;
	return (a);
}
